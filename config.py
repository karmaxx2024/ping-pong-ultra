def validate_config():
    """
    Проверить корректность конфигурации
    
    :return: True если все настройки корректны
    """
    errors = []
    
    # Проверка размеров экрана
    if SCREEN_WIDTH < 640 or SCREEN_HEIGHT < 480:
        errors.append("Минимальный размер экрана: 640x480")
    
    # Проверка FPS
    if FPS < 30 or FPS > 144:
        errors.append("FPS должен быть от 30 до 144")
    
    # Проверка порта
    if SERVER_PORT < 1024 or SERVER_PORT > 65535:
        errors.append("Порт должен быть от 1024 до 65535")
    
    # Проверка очков для победы
    if WIN_SCORE < 1 or WIN_SCORE > 100:
        errors.append("Очков для победы должно быть от 1 до 100")
    
    if errors:
        print("⚠️  Ошибки конфигурации:")
        for error in errors:
            print(f"  - {error}")
        return False
    
    print("✅ Конфигурация проверена успешно!")
    return True

